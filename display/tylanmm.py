line = input()
while line != 'end':
    for i in range(7):
        res = []
        for digit in line:
            if digit == ':':
                res.append(symbols[10][i])
            else:
                res.append(symbols[int(digit)][i])
        print('  '.join(res))
    print('\n')
    line = input()
